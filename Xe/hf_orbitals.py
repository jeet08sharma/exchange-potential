# Orbital occupations (closed-shell, spin-restricted)
OCC_S = 2    # each s orbital: 2 electrons
OCC_P = 6    # each p orbital: 2*(2*1+1) = 6 electrons
OCC_D = 10   # each d orbital: 2*(2*2+1) = 10 electrons

#~~~~~~~~~~~~~~~~~~~~~~~~~~~~ 2. Radial grid ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~

def make_log_grid(r0=1e-6, rmax=40.0, nmax=4000):
    """
    Logarithmic grid r[i] = r0 * exp(h*i) ; h = log(rmax/r0)/(nmax-1).
    rmax = 40 Bohr and N = 4000 .

    """
    h = np.log(rmax/r0)/(nmax - 1)
    al = np.exp(h)
    r = r0 * al ** np.arange(nmax)
    return r, al, h

#~~~~~~~~~~~~~ 3. Fmoment, Simpson integrator on the log mesh~~~~~~~~~~~~~~~~~~~~~~~~~~~~

def fmoment(moment, fnorm, r, fr):
    """
    Computes (1/fnorm)* integrate(r^2*dr * r^moment * fr(r)) via Simpson's rule.
    """
    al = np.log(r[1]/r[0])
    fm = fr * r ** (moment + 3)
    mmax = len(fm)
    if mmax % 2 == 0:
        mmax -= 1
        fm = fm[:mmax]
    imask = np.arange(mmax) % 2
    esum = np.add.reduce(imask * fm)
    antimask = 1 - imask[1:-1]
    osum = np.add.reduce(antimask * fm[1:-1])
    fmom = al * (4.0*esum + 2.0* + fm[0] + fm[-1]) / 3.0
    f = fmom / fnorm
    return f

#~~~~~~~~~~~~~ 4. STO evaluation ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~

def sto_norms(n: int, zeta: float) -> float:
    N = sqrt((2.0 * zeta) ** (2*n + 1) / factorial(2*n))
    return N

def eval_sto(params, r: np.ndarray):
    R,dR,d2R = np.zeros_like(r), np.zeros_like(r), np.zeros_like(r)
    for (n, zeta, c) in params:
        N_  = sto_norms(n, zeta)
        G = N_ * r **(n-1)*np.exp(-zeta*r)
        dG = ((n-1)/r - zeta)*G
        d2G = ((n-1)/r - zeta)**2 * G - (n-1)/r**2 * G
        R += c * G
        dR += c * dG
        d2R += c * d2G
    
    return R,dR,d2R

#~~~~~~~~~~~~~ 5. Build grid and evaluate all the orbitals~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~

r, al_ratio, h_grid = make_log_grid(r0=1e-6, rmax=40.0, nmax=4000)

R1s, dR1s, d2R1s = eval_sto(Xe_1S, r)
