#~~~~~~~~~~~ 6. Density and its derivatives ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~

n = (OCC_S * (R1s**2 + R2s**2 + R3s**2 + R4s**2 + R5s**2)
       + OCC_P * (R2p**2 + R3p**2 + R4p**2 + R5p**2)
       + OCC_D * (R3d**2 + R4d**2)
        ) / (4.0 * pi)

gradn = gradn = (
    OCC_S * 2.0 * (R1s*dR1s + R2s*dR2s + R3s*dR3s + R4s*dR4s + R5s*dR5s)
  + OCC_P * 2.0 * (R2p*dR2p + R3p*dR3p + R4p*dR4p + R5p*dR5p)
  + OCC_D * 2.0 * (R3d*dR3d + R4d*dR4d)
) / (4.0 * pi)

gradn2 = gradn ** 2    

d2n_dr2 = (
    OCC_S * 2.0 * (dR1s**2 + R1s*d2R1s + dR2s**2 + R2s*d2R2s
                 + dR3s**2 + R3s*d2R3s + dR4s**2 + R4s*d2R4s
                 + dR5s**2 + R5s*d2R5s)
  + OCC_P * 2.0 * (dR2p**2 + R2p*d2R2p + dR3p**2 + R3p*d2R3p
                 + dR4p**2 + R4p*d2R4p + dR5p**2 + R5p*d2R5p)
  + OCC_D * 2.0 * (dR3d**2 + R3d*d2R3d + dR4d**2 + R4d*d2R4d)
) / (4.0 * pi)

lapl = d2n_dr2 + (2.0 / r) * gradn

#~~~~~~~~~~~~~ 7. Kinetic enrgy densities ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~

